#include "mapa.h"

int Mapa::getLins() const
{
    return lins;
}

int Mapa::getCols() const
{
    return cols;
}

Mapa::Mapa()
{
    lins = 17;
    cols = 41;
    this->m = new std::string[lins];
    int i = 0;
    this->m[i++] = "###########$#############################";
    this->m[i++] = "$-#---#-----#---------#-#-----#---------#";
    this->m[i++] = "#-###-###-###-#####-###-#-#-#-###-#-#-###";
    this->m[i++] = "#-#-#-------------#-#-----#-#-#-#-#-#-#-#";
    this->m[i++] = "#-#-###-#####-###-#-#-#####-#-#-###-###-#";
    this->m[i++] = "#---#-----#-#---#---------#-#---------#-#";
    this->m[i++] = "###-#-#####-###########-###-##--#-#-###-#";
    this->m[i++] = "#-#-#-#-----#---#---#-----------#-#-#---$";
    this->m[i++] = "#-#-#-#-###-#-#-#-###-###-#-#####-#-#-#-#";
    this->m[i++] = "#-#-#---#-#-#-#---#-----#-#-----#-#-#-#-#";
    this->m[i++] = "#-#-###-#-#-#-#-#-#-#-###-#-#-#-#######-#";
    this->m[i++] = "#-#-------#---#-#-#-#---#-#-#-#-#-#-#-#-#";
    this->m[i++] = "#-#######-#-###-#-#-###-#-#-#-###-#-#-#-#";
    this->m[i++] = "#-#-#-----#-#-#-#-----#-#-#-#-#-#-#-----#";
    this->m[i++] = "#-#-#####-#-#-#-#############-#-#-#####-#";
    this->m[i++] = "#-------------#-------------#-----------#";
    this->m[i++] = "#########################################";

}

bool Mapa::acessivel(int i, int j)
{
    if (i >= 0 && i < lins && j >= 0 && j < cols && this->m[i][j] != '#') {
        return true;
    } else {
        return false;
    }
}

bool Mapa::parede(int i, int j)
{
    if (i >= 0 && i < lins && j >= 0 && j < cols && this->m[i][j] == '#') {
        return true;
    } else {
        return false;
    }
}

bool Mapa::saida(int i, int j)
{
    if (i >= 0 && i < lins && j >= 0 && j < cols && this->m[i][j] == '$') {
        return true;
    } else {
        return false;
    }
}
